import Foundation
import ObjectMapper

class AutographModel: BaseModel {
    var autograph:String?
    
    required init?(map: Map) {
        super.init(map: map)
    }
    
    override func mapping(map: Map) {
        super.mapping(map: map)
        autograph<-map["data"]
    }
}
