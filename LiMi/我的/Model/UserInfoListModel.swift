import ObjectMapper

class UserInfoListModel: BaseModel {
    var userInfos:[UserInfoModel]?
    
    required init?(map: Map) {
        super.init(map: map)
    }
    
    override func mapping(map: Map) {
        super.mapping(map: map)
        userInfos<-map["data"]
    }
}
